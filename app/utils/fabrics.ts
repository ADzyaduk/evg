export function coatingBadgeClass(coating: string): string {
  if (coating.startsWith('ПУ'))
    return 'bg-emerald-500/10 text-emerald-700 ring-1 ring-emerald-500/25 dark:text-emerald-400'
  const map: Record<string, string> = {
    ВО: 'bg-blue-500/10 text-blue-700 ring-1 ring-blue-500/25 dark:text-blue-400',
    НМВО: 'bg-amber-500/10 text-amber-700 ring-1 ring-amber-500/25 dark:text-amber-400',
    ПВХ: 'bg-red-500/10 text-red-700 ring-1 ring-red-500/25 dark:text-red-400',
    Milky: 'bg-purple-500/10 text-purple-700 ring-1 ring-purple-500/25 dark:text-purple-400',
    Мембрана: 'bg-cyan-500/10 text-cyan-700 ring-1 ring-cyan-500/25 dark:text-cyan-400',
    Рипстоп: 'bg-yellow-500/10 text-yellow-700 ring-1 ring-yellow-500/25 dark:text-yellow-400',
    Пухосдерживание: 'bg-sky-500/10 text-sky-700 ring-1 ring-sky-500/25 dark:text-sky-400'
  }
  return map[coating] ?? 'bg-neutral-500/10 text-neutral-600 ring-1 ring-neutral-500/20 dark:text-neutral-400'
}

